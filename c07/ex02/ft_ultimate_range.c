#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;
	int	*result;

	i = max - min;
	result = (int *) malloc(sizeof(int) * i);
	if (min >= max)
		return (0);
	if (range == NULL)
		return (-1);
	*range = result;
	i = 0;
	while (max > min)
	{
		result[i] = min;
		min++;
		i++;
	}
	return (i);
}
